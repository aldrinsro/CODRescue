/**
 * Gestionnaire du bouton hamburger pour l'interface Superpreparation
 * Fichier JavaScript s√©par√© pour √©viter les conflits
 * Version: 1.0.0
 */

(function() {
    'use strict';

    // Configuration
    const CONFIG = {
        DESKTOP_BREAKPOINT: 1024,
        MOBILE_BREAKPOINT: 1023,
        ANIMATION_DURATION: 300,
        DEBUG: true
    };

    // Variables globales du module
    let isInitialized = false;
    let elements = {};

    /**
     * Fonction de logging pour le debug
     */
    function log(message, data = {}) {
        if (!CONFIG.DEBUG) return;
        
        const timestamp = new Date().toISOString();
        const pageUrl = window.location.pathname;
        console.log(`üçî [HamburgerMenu-Superpreparation] [${timestamp}] [${pageUrl}] ${message}`, data);
    }

    /**
     * R√©cup√©ration des √©l√©ments DOM
     */
    function getElements() {
        return {
            body: document.body,
            menuToggle: document.getElementById('menu-toggle'),
            sidebar: document.getElementById('sidebar'),
            sidebarToggle: document.getElementById('sidebar-toggle'),
            contentOverlay: document.getElementById('content-overlay')
        };
    }

    /**
     * V√©rification de la disponibilit√© des √©l√©ments requis
     */
    function validateElements() {
        const required = ['body', 'menuToggle', 'sidebar'];
        const missing = required.filter(key => !elements[key]);
        
        if (missing.length > 0) {
            log('‚ùå √âl√©ments manquants:', missing);
            return false;
        }
        
        log('‚úÖ Tous les √©l√©ments requis sont pr√©sents');
        return true;
    }

    /**
     * Diagnostic complet du DOM
     */
    function diagnosticDOM() {
        const diagnostic = {
            elements: {},
            window: {
                width: window.innerWidth,
                height: window.innerHeight,
                isDesktop: window.innerWidth >= CONFIG.DESKTOP_BREAKPOINT
            },
            body: {
                classes: document.body.className,
                hasOpenClass: document.body.classList.contains('sidebar-open'),
                hasCollapsedClass: document.body.classList.contains('sidebar-collapsed')
            }
        };

        // Diagnostic de chaque √©l√©ment
        Object.keys(elements).forEach(key => {
            const element = elements[key];
            diagnostic.elements[key] = {
                exists: !!element,
                id: element?.id,
                classes: element?.className,
                tagName: element?.tagName
            };

            if (key === 'sidebar' && element) {
                const styles = window.getComputedStyle(element);
                diagnostic.elements[key].styles = {
                    width: styles.width,
                    transform: styles.transform,
                    position: styles.position,
                    display: styles.display,
                    visibility: styles.visibility
                };
            }
        });

        log('üîç Diagnostic DOM complet', diagnostic);
        return diagnostic;
    }

    /**
     * Toggle de la sidebar avec logs d√©taill√©s
     */
    function toggleSidebar() {
        log('üñ±Ô∏è toggleSidebar appel√©e');
        
        if (!elements.sidebar || !elements.body) {
            log('‚ùå √âl√©ments manquants pour le toggle');
            return;
        }

        // Capture de l'√©tat avant
        const beforeState = {
            bodyClasses: elements.body.className,
            sidebarWidth: window.getComputedStyle(elements.sidebar).width,
            sidebarTransform: window.getComputedStyle(elements.sidebar).transform,
            isOpen: elements.body.classList.contains('sidebar-open'),
            isCollapsed: elements.body.classList.contains('sidebar-collapsed')
        };

        // Toggle des classes
        elements.body.classList.toggle('sidebar-open');
        elements.body.classList.toggle('sidebar-collapsed');

        // Toggle de l'animation du hamburger
        if (elements.menuToggle) {
            elements.menuToggle.classList.toggle('hamburger-active');
        }

        // Forcer un recalcul des styles
        elements.sidebar.offsetWidth;

        // Capture de l'√©tat apr√®s
        const afterState = {
            bodyClasses: elements.body.className,
            sidebarWidth: window.getComputedStyle(elements.sidebar).width,
            sidebarTransform: window.getComputedStyle(elements.sidebar).transform,
            isOpen: elements.body.classList.contains('sidebar-open'),
            isCollapsed: elements.body.classList.contains('sidebar-collapsed')
        };

        log('üìä Toggle sidebar effectu√©', {
            before: beforeState,
            after: afterState,
            windowWidth: window.innerWidth,
            isDesktop: window.innerWidth >= CONFIG.DESKTOP_BREAKPOINT
        });

        // Gestion de l'overlay sur mobile
        updateOverlay();
    }

    /**
     * Fermeture de la sidebar sur mobile
     */
    function closeSidebarMobile() {
        const shouldClose = window.innerWidth <= CONFIG.MOBILE_BREAKPOINT;
        
        log('üì± closeSidebarMobile appel√©e', {
            windowWidth: window.innerWidth,
            shouldClose: shouldClose
        });

        if (shouldClose && elements.body) {
            elements.body.classList.add('sidebar-collapsed');
            elements.body.classList.remove('sidebar-open');
            
            // Retirer l'animation du hamburger
            if (elements.menuToggle) {
                elements.menuToggle.classList.remove('hamburger-active');
            }
            
            updateOverlay();
            
            log('‚úÖ Sidebar ferm√©e sur mobile');
        }
    }

    /**
     * Mise √† jour de l'overlay
     */
    function updateOverlay() {
        if (!elements.contentOverlay) return;

        const isOpen = elements.body.classList.contains('sidebar-open');
        const isMobile = window.innerWidth <= CONFIG.MOBILE_BREAKPOINT;
        
        if (isOpen && isMobile) {
            elements.contentOverlay.style.display = 'block';
            // Forcer l'opacit√© apr√®s un petit d√©lai pour l'animation
            setTimeout(() => {
                elements.contentOverlay.style.opacity = '1';
            }, 10);
        } else {
            elements.contentOverlay.style.opacity = '0';
            setTimeout(() => {
                elements.contentOverlay.style.display = 'none';
            }, CONFIG.ANIMATION_DURATION);
        }
    }

    /**
     * D√©finition de l'√©tat initial de la sidebar
     */
    function setInitialState() {
        if (!elements.body) return;

        const isDesktop = window.innerWidth >= CONFIG.DESKTOP_BREAKPOINT;
        
        log('üìè D√©finition √©tat initial', {
            windowWidth: window.innerWidth,
            isDesktop: isDesktop
        });

        // Nettoyer les classes existantes
        elements.body.classList.remove('sidebar-open', 'sidebar-collapsed');

        if (isDesktop) {
            elements.body.classList.add('sidebar-open');
            log('üíª √âtat initial: desktop (sidebar ouverte)');
        } else {
            elements.body.classList.add('sidebar-collapsed');
            log('üì± √âtat initial: mobile (sidebar ferm√©e)');
        }

        updateOverlay();
    }

    /**
     * Gestion du redimensionnement de la fen√™tre
     */
    function handleResize() {
        const newWidth = window.innerWidth;
        const isDesktop = newWidth >= CONFIG.DESKTOP_BREAKPOINT;
        
        log('üìè Redimensionnement d√©tect√©', {
            newWidth: newWidth,
            isDesktop: isDesktop
        });

        if (!elements.body) return;

        if (isDesktop) {
            elements.body.classList.add('sidebar-open');
            elements.body.classList.remove('sidebar-collapsed');
            log('üíª Passage en mode desktop');
        } else {
            elements.body.classList.add('sidebar-collapsed');
            elements.body.classList.remove('sidebar-open');
            log('üì± Passage en mode mobile');
        }

        updateOverlay();
    }

    /**
     * Attachement des event listeners
     */
    function attachEventListeners() {
        log('üîó Attachement des event listeners');

        // Listener principal du bouton hamburger
        if (elements.menuToggle) {
            // Supprimer les listeners existants en clonant l'√©l√©ment
            const newMenuToggle = elements.menuToggle.cloneNode(true);
            elements.menuToggle.parentNode.replaceChild(newMenuToggle, elements.menuToggle);
            elements.menuToggle = newMenuToggle;

            elements.menuToggle.addEventListener('click', function(e) {
                log('üñ±Ô∏è Clic sur menu-toggle d√©tect√©', {
                    event: {
                        type: e.type,
                        target: e.target.id,
                        timestamp: e.timeStamp
                    }
                });
                
                e.preventDefault();
                e.stopPropagation();
                toggleSidebar();
            });

            log('‚úÖ Event listener attach√© sur menuToggle');
        }

        // Listener pour fermer via le bouton X de la sidebar
        if (elements.sidebarToggle) {
            elements.sidebarToggle.addEventListener('click', closeSidebarMobile);
            log('‚úÖ Event listener attach√© sur sidebarToggle');
        }

        // Listener pour fermer via l'overlay
        if (elements.contentOverlay) {
            elements.contentOverlay.addEventListener('click', closeSidebarMobile);
            log('‚úÖ Event listener attach√© sur contentOverlay');
        }

        // Listener pour le redimensionnement
        window.addEventListener('resize', handleResize);
        log('‚úÖ Event listener attach√© sur window.resize');
    }

    /**
     * Initialisation du module
     */
    function initialize() {
        if (isInitialized) {
            log('‚ö†Ô∏è Module d√©j√† initialis√©, skip');
            return false;
        }

        log('üîß D√©but d\'initialisation du module hamburger Superpreparation');

        try {
            // R√©cup√©ration des √©l√©ments
            elements = getElements();
            
            // Validation
            if (!validateElements()) {
                log('‚ùå Validation √©chou√©e, abandon de l\'initialisation');
                return false;
            }

            // Diagnostic initial
            diagnosticDOM();

            // Configuration de l'√©tat initial
            setInitialState();

            // Attachement des event listeners
            attachEventListeners();

            // Marquer comme initialis√©
            isInitialized = true;

            log('‚úÖ Initialisation termin√©e avec succ√®s');
            return true;

        } catch (error) {
            log('üí• Erreur lors de l\'initialisation', {
                error: error.message,
                stack: error.stack,
                name: error.name
            });
            return false;
        }
    }

    /**
     * Fonction de r√©initialisation (pour debug)
     */
    function reinitialize() {
        log('üîÑ R√©initialisation forc√©e');
        isInitialized = false;
        elements = {};
        initialize();
    }

    /**
     * API publique du module
     */
    window.HamburgerMenuSuperpreparation = {
        // M√©thodes principales
        init: initialize,
        reinit: reinitialize,
        toggle: toggleSidebar,
        close: closeSidebarMobile,
        
        // M√©thodes de debug
        diagnostic: diagnosticDOM,
        getElements: () => elements,
        getConfig: () => CONFIG,
        
        // Statut
        isInitialized: () => isInitialized
    };

    /**
     * Auto-initialisation
     */
    function autoInit() {
        log('üöÄ Auto-initialisation du module Superpreparation');

        // Tentative 1: DOMContentLoaded
        if (document.readyState === 'loading') {
            log('üìÑ DOM en cours de chargement, attente DOMContentLoaded');
            document.addEventListener('DOMContentLoaded', initialize);
        } else {
            log('üìÑ DOM d√©j√† charg√©, initialisation imm√©diate');
            initialize();
        }

        // Tentatives de fallback
        setTimeout(() => {
            if (!isInitialized) {
                log('‚è∞ Tentative fallback (100ms)');
                initialize();
            }
        }, 100);

        setTimeout(() => {
            if (!isInitialized) {
                log('‚è∞ Tentative fallback (500ms)');
                initialize();
            }
        }, 500);

        setTimeout(() => {
            if (!isInitialized) {
                log('üí• √âCHEC: Module non initialis√© apr√®s 1s');
                diagnosticDOM();
            }
        }, 1000);
    }

    // D√©marrage automatique
    autoInit();

})();
